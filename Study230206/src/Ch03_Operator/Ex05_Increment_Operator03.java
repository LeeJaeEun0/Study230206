package Ch03_Operator;

public class Ex05_Increment_Operator03 {

	public static void main(String[] args) {
		int i = 5, j=5;
		System.out.println(i++); // 이전 값을 출력하고 더함
		System.out.println(++j); // 더한 상태를 출력
		System.out.println("i = "+ i+ ", j ="+ j);

	} // end of main

} // end of class
