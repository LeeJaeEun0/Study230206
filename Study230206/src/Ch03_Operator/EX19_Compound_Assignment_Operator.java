package Ch03_Operator;

public class EX19_Compound_Assignment_Operator {

	public static void main(String[] args) {
		// 대입 연산자는 다른 연산자와 결합하여 사용가능
		int i=0;
		
		i += 3;//i = i+3;와 같다
		
		i -= 3;//i = i-3;와 같다
		
		i *= 3;//i = i*3;와 같다
		
		i /= 3;//i = i/3;와 같다
	}// end of main

} // end of class
