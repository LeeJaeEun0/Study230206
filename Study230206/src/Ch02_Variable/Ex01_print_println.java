package Ch02_Variable;

public class Ex01_print_println {

	public static void main(String[] args) {		
		System.out.println("Hello, world"); // 괄호 내용을 출력하고 줄바꿈을 함
		System.out.print("Hello, "); // 괄호 안의 내용을 출력하고 줄바꿍을 하지 않음
		System.out.println("world");
		System.out.println();  // 내용이 비어 있으면 한 줄 아래로 줄바꿈
		
		System.out.println("Hello, world");
		System.out.print("3+5 = ");
		System.out.println(3+5);
	} // end of main

} // end of class
