package Ch02_Variable;

public class Ex03_Variable01 {

	public static void main(String[] args) {
		// 변수는 하나의 값을 저장할 수 있는 공간
		int x =5; // 변수 선언
		System.out.println(x);
		
		// 변수는 하나의 값만 저장 할 수 있어서 기존 값은 지워지고 새로 저장된 값만 남는다.
		x=10;
		System.out.println(x);
		
	} // end of main

} // end of class
