package Ch02_Variable;

public class Ex02_print_add_sub {

	public static void main(String[] args) {
		System.out.println(5+3); // 더하기의 결과가 출력 됨
		System.out.println(5-3); // 빼기의 결과가 출력 됨
		System.out.println(5*3); // 곱하기의 결과가 출력 됨
		System.out.println(5/3); // 나누기의 결과가 출력 됨

	} // end of main

} // end of class
