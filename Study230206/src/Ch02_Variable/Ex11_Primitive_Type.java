package Ch02_Variable;

public class Ex11_Primitive_Type {

	public static void main(String[] args) {
		// 기본형에는 8가지의 타입(자료형)이 있다.
		// 정수형, 실수형, 문자형, 논리형으로 크게 구분 가능
		System.out.println("short - 2byte, int - 4byte, long - 8byte");
		System.out.println("float - 4byte, double - 8byte");
		System.out.println("char는 자바에서 유니코드를 사용 - 2byte");
		System.out.println("boolean은 true와 false 값만 표현 가능 - 1byte");
	}// end of main

} // end of class
