package Ch02_Variable;

public class Ex04_Variable_Type {

	public static void main(String[] args) {
		
		int x = 100; // 정수형
		double pi =3.14; // 실수형
		char ch = 'a'; // 문자형 - 문자 1개
		String str = "ABC"; // 문자열
		
		// 변수형은 변수의 저장할 값의 종류에 따라 맞는 타입을 선택
		
		System.out.println(x);
		System.out.println(pi);
		System.out.println(ch);
		System.out.println(str);

	} // end of main

} // end of class
