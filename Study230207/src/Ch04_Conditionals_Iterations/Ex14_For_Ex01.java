package Ch04_Conditionals_Iterations;

public class Ex14_For_Ex01 {

	public static void main(String[] args) {
		for(int i=1; i<=5; i++) // 세로로 출력 - 반복문
			System.out.println(i);
		
		for(int i=1; i<=5; i++) // 가로로출력 - 반복문
			System.out.print(i);
		
		System.out.println();
	} // end of main

} // end of class
